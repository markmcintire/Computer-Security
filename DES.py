def permute(key, table):
    #permutation_order = [item for item in table]
    
    permuted_key = []
    for index in table:
        #print(index)
        permuted_key.append(key[index - 1])
    return permuted_key

def left_rotate(key, shifts):
    #shift key left by given amount 
    return key[shifts:] + key[:shifts]

def xor(arr1, arr2):
    #xor array
    return [int(bit1) ^ int(bit2) for bit1, bit2 in zip(arr1, arr2)]

